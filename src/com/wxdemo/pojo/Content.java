package com.wxdemo.pojo;

public class Content {
	private int id;
	private String name;
	private String imgSrc;
	private String url;
	private int classMasterId;
	private String cimg;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the imgSrc
	 */
	public String getImgSrc() {
		return imgSrc;
	}
	/**
	 * @param imgSrc the imgSrc to set
	 */
	public void setImgSrc(String imgSrc) {
		this.imgSrc = imgSrc;
	}
	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	/**
	 * @return the classMasterId
	 */
	public int getClassMasterId() {
		return classMasterId;
	}
	/**
	 * @param classMasterId the classMasterId to set
	 */
	public void setClassMasterId(int classMasterId) {
		this.classMasterId = classMasterId;
	}
	/**
	 * @return the cimg
	 */
	public String getCimg() {
		return cimg;
	}
	/**
	 * @param cimg the cimg to set
	 */
	public void setCimg(String cimg) {
		this.cimg = cimg;
	}
	
	
}
